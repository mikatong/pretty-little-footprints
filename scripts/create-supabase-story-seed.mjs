import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const storiesPath = resolve("src/data/stories.ts");
const outputPath = resolve("supabase/seed-stories.sql");
const ownerUserId = process.env.SUPABASE_OWNER_USER_ID ?? "00000000-0000-0000-0000-000000000000";

function getArrayBody(source, marker) {
  const start = source.indexOf(marker);
  if (start === -1) throw new Error(`Could not find ${marker}`);
  const bracket = source.indexOf("[", start + marker.length - 1);
  let depth = 0;
  let quote = "";
  let escape = false;

  for (let index = bracket; index < source.length; index += 1) {
    const char = source[index];
    if (quote) {
      if (escape) escape = false;
      else if (char === "\\") escape = true;
      else if (char === quote) quote = "";
      continue;
    }
    if (char === "\"" || char === "'" || char === "`") {
      quote = char;
      continue;
    }
    if (char === "[") depth += 1;
    if (char === "]") {
      depth -= 1;
      if (depth === 0) return source.slice(bracket + 1, index);
    }
  }
  throw new Error("Could not parse stories array.");
}

function splitObjects(body) {
  const objects = [];
  let start = -1;
  let depth = 0;
  let quote = "";
  let escape = false;

  for (let index = 0; index < body.length; index += 1) {
    const char = body[index];
    if (quote) {
      if (escape) escape = false;
      else if (char === "\\") escape = true;
      else if (char === quote) quote = "";
      continue;
    }
    if (char === "\"" || char === "'" || char === "`") {
      quote = char;
      continue;
    }
    if (char === "{") {
      if (depth === 0) start = index;
      depth += 1;
    }
    if (char === "}") {
      depth -= 1;
      if (depth === 0 && start >= 0) objects.push(body.slice(start, index + 1));
    }
  }

  return objects;
}

function stringProp(objectSource, key) {
  const match = objectSource.match(new RegExp(`${key}:\\s*([\"'\`])([\\s\\S]*?)\\1`));
  return match?.[2] ?? "";
}

function boolProp(objectSource, key) {
  const match = objectSource.match(new RegExp(`${key}:\\s*(true|false)`));
  return match?.[1] === "true";
}

function firstTextBody(objectSource) {
  const match = objectSource.match(/type:\s*"text"[\s\S]*?body:\s*([`"'])([\s\S]*?)\1/);
  return match?.[2] ?? "";
}

function galleryUrls(objectSource) {
  return [...objectSource.matchAll(/src:\s*"([^"]+)"/g)]
    .map((match) => match[1])
    .filter((src) => src.startsWith("/images/stories/") && !src.includes("/cover."));
}

function sqlString(value) {
  if (!value) return "null";
  return `'${value.replace(/'/g, "''")}'`;
}

const source = readFileSync(storiesPath, "utf8");
const storyObjects = splitObjects(getArrayBody(source, "export const stories: Story[] = ["));
const values = storyObjects.map((objectSource) => {
  const slug = stringProp(objectSource, "slug");
  const placeId = stringProp(objectSource, "placeId");
  const title = stringProp(objectSource, "title");
  const status = stringProp(objectSource, "status") || "draft";
  const previewSummary = stringProp(objectSource, "previewSummary");
  const coverUrl = stringProp(objectSource, "coverImage");
  const galleries = JSON.stringify(galleryUrls(objectSource));

  return `  (${sqlString(slug)}, ${sqlString(placeId)}, ${sqlString(title)}, ${sqlString(previewSummary)}, ${sqlString(firstTextBody(objectSource))}, ${sqlString(status)}, ${boolProp(objectSource, "featured")}, ${sqlString(coverUrl)}, '${galleries}'::jsonb, '${ownerUserId}'::uuid)`;
});

const sql = `-- Generated by npm run seed:stories.
-- Set SUPABASE_OWNER_USER_ID to your Supabase auth.users.id before running.

insert into public.stories (
  slug,
  place_id,
  title,
  preview_summary,
  body,
  status,
  featured,
  cover_url,
  gallery_urls,
  user_id
)
values
${values.join(",\n")}
on conflict (slug) do update set
  place_id = excluded.place_id,
  title = excluded.title,
  preview_summary = excluded.preview_summary,
  body = excluded.body,
  status = excluded.status,
  featured = excluded.featured,
  cover_url = excluded.cover_url,
  gallery_urls = excluded.gallery_urls,
  user_id = excluded.user_id,
  updated_at = now();
`;

writeFileSync(outputPath, sql);
console.log(`Wrote ${outputPath}`);
